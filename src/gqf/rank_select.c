#include "gqf/rank_select.h"


uint64_t word_rank(uint64_t val) {
	return popcnt(val);
}

uint64_t word_select(uint64_t val, int rank) {
	return bitselect(val, rank);
}

int popcnt(uint64_t val)
{
	asm("popcnt %[val], %[val]"
			: [val] "+r" (val)
			:
			: "cc");
	return val;
}

